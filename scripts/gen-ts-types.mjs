#!/usr/bin/env node
// PhoneMic 协议镜像 fingerprint 守护脚本
//
// 任务来源：.kiro/specs/phone-mic-voice-input/tasks.md 2.5
// 设计来源：.kiro/specs/phone-mic-voice-input/design.md §5
//
// 这是一个「完整性守护」脚本，不是代码生成器。本仓库选择手工维护 TS
// 镜像（理由见 apps/mobile/src/protocol/README.md），由本脚本计算并写
// 入一个 fingerprint stamp 文件，CI 通过 `--check` 模式重新计算 fingerprint
// 并比对，从而拦截 Rust 与 TS 字面量漂移。
//
// 用法：
//   node scripts/gen-ts-types.mjs            # 重写 stamp（本地运行）
//   node scripts/gen-ts-types.mjs --check    # 仅校验（CI 使用）
//
// 退出码：
//   0  stamp 与当前源文件一致
//   1  fingerprint 漂移、stamp 缺失或源文件缺失
//
// 维护流程（与 README 同步）：
//   1) 编辑 Rust 类型；
//   2) 同步更新 TS 镜像文件；
//   3) 运行 `pnpm gen:ts-types` 刷新 stamp；
//   4) 提交所有改动。

import { writeFile, readFile, mkdir } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

import {
  PROTOCOL_SOURCES,
  computeProtocolFingerprints,
  ensureProtocolSourcesExist,
} from './lib/protocol-fingerprint.mjs'

const STAMP_REL_PATH = 'apps/mobile/src/protocol/.protocol-stamp.json'
const PROTOCOL_VERSION = '1'

/** @type {string} 仓库根（脚本位于 scripts/，向上一级即可） */
const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')

const args = process.argv.slice(2)
const checkMode = args.includes('--check')

async function readStampIfExists() {
  const abs = path.resolve(REPO_ROOT, STAMP_REL_PATH)
  try {
    const text = await readFile(abs, 'utf8')
    return JSON.parse(text)
  } catch (err) {
    if (err && err.code === 'ENOENT') return null
    throw err
  }
}

function buildStamp({ rust, ts }) {
  return {
    /* eslint-disable */
    $note: [
      'Generated by scripts/gen-ts-types.mjs (task 2.5).',
      'Two fingerprints (rust + ts) are computed deterministically from the',
      'protocol source files; CI re-computes them and fails on mismatch.',
      'Do not edit by hand — run `pnpm gen:ts-types` to refresh.',
    ],
    /* eslint-enable */
    protocolVersion: PROTOCOL_VERSION,
    algorithm: 'sha256-deterministic-v1',
    sources: {
      rust: PROTOCOL_SOURCES.rust,
      ts: PROTOCOL_SOURCES.ts,
    },
    fingerprints: { rust, ts },
  }
}

async function writeStamp(stamp) {
  const abs = path.resolve(REPO_ROOT, STAMP_REL_PATH)
  await mkdir(path.dirname(abs), { recursive: true })
  // 末尾保留 \n，避免编辑器自动加换行造成与脚本输出的字节差异。
  await writeFile(abs, `${JSON.stringify(stamp, null, 2)}\n`, 'utf8')
}

async function main() {
  await ensureProtocolSourcesExist(REPO_ROOT)
  const fingerprints = await computeProtocolFingerprints(REPO_ROOT)
  const expectedStamp = buildStamp(fingerprints)

  if (!checkMode) {
    await writeStamp(expectedStamp)
    process.stdout.write(
      `protocol stamp updated: rust=${fingerprints.rust.slice(0, 12)}… ts=${fingerprints.ts.slice(0, 12)}…\n`,
    )
    return
  }

  const existing = await readStampIfExists()
  if (!existing) {
    process.stderr.write(
      `error: ${STAMP_REL_PATH} is missing. Run \`pnpm gen:ts-types\` to create it.\n`,
    )
    process.exit(1)
  }

  const expectedRust = expectedStamp.fingerprints.rust
  const expectedTs = expectedStamp.fingerprints.ts
  const actualRust = existing?.fingerprints?.rust
  const actualTs = existing?.fingerprints?.ts
  const expectedVersion = expectedStamp.protocolVersion
  const actualVersion = existing?.protocolVersion

  const mismatches = []
  if (actualVersion !== expectedVersion) {
    mismatches.push(
      `protocolVersion: stamp=${JSON.stringify(actualVersion)} expected=${JSON.stringify(
        expectedVersion,
      )}`,
    )
  }
  if (actualRust !== expectedRust) {
    mismatches.push(`rust: stamp=${actualRust ?? '<missing>'} expected=${expectedRust}`)
  }
  if (actualTs !== expectedTs) {
    mismatches.push(`ts:   stamp=${actualTs ?? '<missing>'} expected=${expectedTs}`)
  }

  if (mismatches.length > 0) {
    process.stderr.write('error: protocol stamp drift detected.\n')
    for (const line of mismatches) process.stderr.write(`  - ${line}\n`)
    process.stderr.write(
      "hint: run `pnpm gen:ts-types` after updating both Rust types and TS mirrors.\n",
    )
    process.exit(1)
  }

  process.stdout.write('protocol stamp ok\n')
}

main().catch((err) => {
  process.stderr.write(`gen-ts-types failed: ${err && err.stack ? err.stack : err}\n`)
  process.exit(1)
})
